#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	unsigned char	uc;
	char			*tmp;

	tmp = NULL;
	uc = (unsigned char)c;
	while (*s != '\0')
	{
		if (*s == uc)
			tmp = (char *)s;
		s++;
	}
	if (uc == '\0')
		return ((char *)s);
	return (tmp);
}
