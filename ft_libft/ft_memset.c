#include "libft.h"

void	*ft_memset(void *ptr, int x, size_t n)
{
	size_t			i;
	unsigned char	*char_ptr;

	char_ptr = ptr;
	i = 0;
	while (i < n)
	{
		char_ptr[i] = (unsigned char)x;
		i++;
	}
	return (ptr);
}
